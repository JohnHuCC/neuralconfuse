clear,clc;  %清除前面資料
x = -1 + (1-(-1))*rand(121,1);  %-1~1之間121筆
y = -1 + (1-(-1))*rand(121,1);
w1=1;
w2=1;
w3=1;
c1=0;
c2=0;
c3=0;
a1=1;
a2=1;
a3=1;
g1 = -((x-c1).^2+(y-c1).^2)./(2*a1).^2;  %陣列加減乘除平方每個括號後面都要加.
g2 = -((x-c2).^2+(y-c2).^2)./(2*a2).^2; 
g3 = -((x-c3).^2+(y-c3).^2)./(2*a3).^2; 
z=w1*g1+w2*g2+w3*g3 %z(網路輸出值)
r=cos(3*x).*sin(2*y) %r(實際值)
t = 1:121; %設置121個點
plot(t,z, t,r); %t對z(網路輸出值)  t對r(實際值)
xlabel('筆數');
ylabel('函數值');
legend('預測值','實際值');
plot(x,z,'--*',x,r,':o');